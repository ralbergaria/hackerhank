package hackerhank.dataStructures;

import java.util.Scanner;

public class Solution_arraylist {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();
		for (int i = 0; i < n; i++){
			String line = sc.next();			
			System.out.println(line);
		}		
	}
}
